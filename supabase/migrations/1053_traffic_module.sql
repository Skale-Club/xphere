-- ---------------------------------------------------------------------------
-- Section 1: traffic_setups
-- One row per organization. Tracks installation state and script token.
-- ---------------------------------------------------------------------------

CREATE TABLE public.traffic_setups (
  id                  UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
  organization_id     UUID        NOT NULL UNIQUE REFERENCES public.organizations(id) ON DELETE CASCADE,
  script_token        TEXT        NOT NULL UNIQUE DEFAULT encode(gen_random_bytes(24), 'hex'),
  primary_website_url TEXT,
  verification_state  TEXT        NOT NULL DEFAULT 'not_started'
                      CHECK (verification_state IN ('not_started','pending','verified','failed','no_events_yet')),
  verified_at         TIMESTAMPTZ,
  gtm_container_id    TEXT,
  created_at          TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at          TIMESTAMPTZ NOT NULL DEFAULT now()
);

ALTER TABLE public.traffic_setups ENABLE ROW LEVEL SECURITY;

CREATE POLICY "traffic_setups_select" ON public.traffic_setups
  FOR SELECT TO authenticated
  USING (organization_id = (SELECT public.get_current_org_id()));

CREATE POLICY "traffic_setups_insert" ON public.traffic_setups
  FOR INSERT TO authenticated
  WITH CHECK (organization_id = (SELECT public.get_current_org_id()));

CREATE POLICY "traffic_setups_update" ON public.traffic_setups
  FOR UPDATE TO authenticated
  USING (organization_id = (SELECT public.get_current_org_id()));

DROP TRIGGER IF EXISTS trg_traffic_setups_set_updated_at ON public.traffic_setups;
CREATE TRIGGER trg_traffic_setups_set_updated_at
  BEFORE UPDATE ON public.traffic_setups
  FOR EACH ROW EXECUTE FUNCTION public.update_updated_at();

-- ---------------------------------------------------------------------------
-- Section 2: traffic_visitors
-- Anonymous visitor fingerprints. visitor_id comes from a persistent cookie
-- set by the tracking script. Identified when linked to a contact.
-- ---------------------------------------------------------------------------

CREATE TABLE public.traffic_visitors (
  id               UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
  organization_id  UUID        NOT NULL REFERENCES public.organizations(id) ON DELETE CASCADE,
  visitor_key      TEXT        NOT NULL,
  first_seen_at    TIMESTAMPTZ NOT NULL DEFAULT now(),
  last_seen_at     TIMESTAMPTZ NOT NULL DEFAULT now(),
  session_count    INTEGER     NOT NULL DEFAULT 0,
  page_view_count  INTEGER     NOT NULL DEFAULT 0,
  is_identified    BOOLEAN     NOT NULL DEFAULT false,
  contact_id       UUID        REFERENCES public.contacts(id) ON DELETE SET NULL,
  created_at       TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at       TIMESTAMPTZ NOT NULL DEFAULT now(),
  UNIQUE (organization_id, visitor_key)
);

ALTER TABLE public.traffic_visitors ENABLE ROW LEVEL SECURITY;

CREATE POLICY "traffic_visitors_select" ON public.traffic_visitors
  FOR SELECT TO authenticated
  USING (organization_id = (SELECT public.get_current_org_id()));

CREATE INDEX idx_traffic_visitors_org ON public.traffic_visitors (organization_id);
CREATE INDEX idx_traffic_visitors_contact ON public.traffic_visitors (contact_id) WHERE contact_id IS NOT NULL;

DROP TRIGGER IF EXISTS trg_traffic_visitors_set_updated_at ON public.traffic_visitors;
CREATE TRIGGER trg_traffic_visitors_set_updated_at
  BEFORE UPDATE ON public.traffic_visitors
  FOR EACH ROW EXECUTE FUNCTION public.update_updated_at();

-- ---------------------------------------------------------------------------
-- Section 3: traffic_sessions
-- One row per browser session. Captures UTM attribution, device, geo, referrer.
-- session_key is a UUID generated by the tracking script per session.
-- ---------------------------------------------------------------------------

CREATE TABLE public.traffic_sessions (
  id               UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
  organization_id  UUID        NOT NULL REFERENCES public.organizations(id) ON DELETE CASCADE,
  visitor_id       UUID        NOT NULL REFERENCES public.traffic_visitors(id) ON DELETE CASCADE,
  session_key      TEXT        NOT NULL,
  started_at       TIMESTAMPTZ NOT NULL DEFAULT now(),
  ended_at         TIMESTAMPTZ,
  page_view_count  INTEGER     NOT NULL DEFAULT 0,
  duration_seconds INTEGER,
  landing_page     TEXT,
  exit_page        TEXT,
  referrer         TEXT,
  -- UTM attribution
  utm_source       TEXT,
  utm_medium       TEXT,
  utm_campaign     TEXT,
  utm_term         TEXT,
  utm_content      TEXT,
  -- Geo (privacy-conscious: city/country only, no precise coordinates)
  country_code     TEXT,
  country_name     TEXT,
  city             TEXT,
  -- Device
  device_type      TEXT        CHECK (device_type IN ('desktop','mobile','tablet','unknown')),
  browser          TEXT,
  os               TEXT,
  -- Conversion
  is_converted     BOOLEAN     NOT NULL DEFAULT false,
  created_at       TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at       TIMESTAMPTZ NOT NULL DEFAULT now(),
  UNIQUE (organization_id, session_key)
);

ALTER TABLE public.traffic_sessions ENABLE ROW LEVEL SECURITY;

CREATE POLICY "traffic_sessions_select" ON public.traffic_sessions
  FOR SELECT TO authenticated
  USING (organization_id = (SELECT public.get_current_org_id()));

CREATE INDEX idx_traffic_sessions_org ON public.traffic_sessions (organization_id);
CREATE INDEX idx_traffic_sessions_visitor ON public.traffic_sessions (visitor_id);
CREATE INDEX idx_traffic_sessions_started_at ON public.traffic_sessions (organization_id, started_at DESC);
CREATE INDEX idx_traffic_sessions_utm_campaign ON public.traffic_sessions (organization_id, utm_campaign) WHERE utm_campaign IS NOT NULL;

DROP TRIGGER IF EXISTS trg_traffic_sessions_set_updated_at ON public.traffic_sessions;
CREATE TRIGGER trg_traffic_sessions_set_updated_at
  BEFORE UPDATE ON public.traffic_sessions
  FOR EACH ROW EXECUTE FUNCTION public.update_updated_at();

-- ---------------------------------------------------------------------------
-- Section 4: traffic_pageviews
-- One row per page view. Linked to session and visitor.
-- ---------------------------------------------------------------------------

CREATE TABLE public.traffic_pageviews (
  id               UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
