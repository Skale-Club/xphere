  url              TEXT        NOT NULL,
  path             TEXT        NOT NULL,
  title            TEXT,
  referrer         TEXT,
  duration_seconds INTEGER,
  occurred_at      TIMESTAMPTZ NOT NULL DEFAULT now(),
  created_at       TIMESTAMPTZ NOT NULL DEFAULT now()
);

ALTER TABLE public.traffic_pageviews ENABLE ROW LEVEL SECURITY;

CREATE POLICY "traffic_pageviews_select" ON public.traffic_pageviews
  FOR SELECT TO authenticated
  USING (organization_id = (SELECT public.get_current_org_id()));

CREATE INDEX idx_traffic_pageviews_org_occurred ON public.traffic_pageviews (organization_id, occurred_at DESC);
CREATE INDEX idx_traffic_pageviews_session ON public.traffic_pageviews (session_id);
CREATE INDEX idx_traffic_pageviews_path ON public.traffic_pageviews (organization_id, path);

-- ---------------------------------------------------------------------------
-- Section 5: traffic_events
-- Conversion and custom events captured by the tracking script or server-side.
-- event_type is a known set; custom events use type='custom_conversion' + event_name.
-- ---------------------------------------------------------------------------

CREATE TABLE public.traffic_events (
  id               UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
  organization_id  UUID        NOT NULL REFERENCES public.organizations(id) ON DELETE CASCADE,
  session_id       UUID        REFERENCES public.traffic_sessions(id) ON DELETE SET NULL,
  visitor_id       UUID        REFERENCES public.traffic_visitors(id) ON DELETE SET NULL,
  event_type       TEXT        NOT NULL
                   CHECK (event_type IN (
                     'form_submit','phone_click','sms_click','call_started',
                     'chat_started','booking_started','booking_completed',
                     'contact_created','opportunity_created','deal_won','custom_conversion'
                   )),
  event_name       TEXT,
  url              TEXT,
  metadata         JSONB       NOT NULL DEFAULT '{}',
  contact_id       UUID        REFERENCES public.contacts(id) ON DELETE SET NULL,
  opportunity_id   UUID        REFERENCES public.opportunities(id) ON DELETE SET NULL,
  occurred_at      TIMESTAMPTZ NOT NULL DEFAULT now(),
  created_at       TIMESTAMPTZ NOT NULL DEFAULT now()
);

ALTER TABLE public.traffic_events ENABLE ROW LEVEL SECURITY;

CREATE POLICY "traffic_events_select" ON public.traffic_events
  FOR SELECT TO authenticated
  USING (organization_id = (SELECT public.get_current_org_id()));

CREATE INDEX idx_traffic_events_org_occurred ON public.traffic_events (organization_id, occurred_at DESC);
CREATE INDEX idx_traffic_events_session ON public.traffic_events (session_id) WHERE session_id IS NOT NULL;
CREATE INDEX idx_traffic_events_type ON public.traffic_events (organization_id, event_type);

-- ---------------------------------------------------------------------------
-- Section 6: traffic_attributions
-- First-touch and last-touch attribution records per visitor.
-- One first row and one last row per visitor (upserted on each session).
-- ---------------------------------------------------------------------------

CREATE TABLE public.traffic_attributions (
  id               UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
  organization_id  UUID        NOT NULL REFERENCES public.organizations(id) ON DELETE CASCADE,
  visitor_id       UUID        NOT NULL REFERENCES public.traffic_visitors(id) ON DELETE CASCADE,
  touch_type       TEXT        NOT NULL CHECK (touch_type IN ('first','last')),
  utm_source       TEXT,
  utm_medium       TEXT,
  utm_campaign     TEXT,
  utm_term         TEXT,
  utm_content      TEXT,
  landing_page     TEXT,
  referrer         TEXT,
  session_id       UUID        REFERENCES public.traffic_sessions(id) ON DELETE SET NULL,
  occurred_at      TIMESTAMPTZ NOT NULL DEFAULT now(),
  created_at       TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at       TIMESTAMPTZ NOT NULL DEFAULT now(),
  UNIQUE (visitor_id, touch_type)
);

ALTER TABLE public.traffic_attributions ENABLE ROW LEVEL SECURITY;

CREATE POLICY "traffic_attributions_select" ON public.traffic_attributions
  FOR SELECT TO authenticated
  USING (organization_id = (SELECT public.get_current_org_id()));

CREATE INDEX idx_traffic_attributions_visitor ON public.traffic_attributions (visitor_id);
CREATE INDEX idx_traffic_attributions_org ON public.traffic_attributions (organization_id);

DROP TRIGGER IF EXISTS trg_traffic_attributions_set_updated_at ON public.traffic_attributions;
CREATE TRIGGER trg_traffic_attributions_set_updated_at
  BEFORE UPDATE ON public.traffic_attributions
  FOR EACH ROW EXECUTE FUNCTION public.update_updated_at();
